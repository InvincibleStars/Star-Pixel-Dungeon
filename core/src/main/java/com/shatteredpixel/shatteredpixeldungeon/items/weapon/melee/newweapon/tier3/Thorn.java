public class Thorn extends MeleeWeapon {

	{
		image = ItemSpriteSheet.THORN;
		hitSound = Assets.Sounds.HIT_SLASH;
		hitSoundPitch = 1.1f;

		tier = 1;
		DLY=1.5f;

	}

	public int damageRoll(Char attacker, Char defender, int damage) {
			if (defender.buff(Bleeding.class)!=null) {
				damage = augment.damageFactor(Random.NormalIntRange(
						min() + Math.round(2f),
						max()));
				return damage;
			}
		return super.damageRoll(defender);
	}

	@Override
	public int proc(Char attacker, Char defender, int damage) {
		if (defender.buff(Bleeding.class)!=null) {
			Buff.affect(defender, Bleeding.class).set(2f);
			Buff.affect(defender, Cripple.class, BuffWait.T2);
			GLog.n( Messages.get(this, "warm") );
		}
		return super.proc(attacker, defender, damage);
	}
}

